def _major_card(card: dict) -> dict:
    return {
        **card,
        "arcana": "major",
        "suit": "major",
        "image": f"/tarot/cards/{card['id']}.png",
    }


def _minor_card(suit_id: str, rank_index: int, rank_data: tuple) -> dict:
    rank_id, rank_en, rank_zh, rank_keywords, rank_core, rank_shadow, rank_hint = rank_data
    suit = SUITS[suit_id]
    card_no = str(rank_index + 1).zfill(2)
    card_id = f"{suit_id}-{card_no}-{rank_id}"
    keywords = [rank_keywords[0], suit["keywords"][1], suit["keywords"][2]]
    return {
        "id": card_id,
        "name_en": f"{rank_en} of {suit['name_en']}",
        "name_zh": f"{suit['name_zh']}{rank_zh}",
        "arcana": "minor",
        "suit": suit_id,
        "rank": rank_id,
        "image": f"/tarot/cards/{card_id}.png",
        "keywords": keywords,
        "core_meaning": f"{suit['name_zh']}关注{suit['core']}；{rank_zh}表示{rank_core}。",
        "shadow_meaning": f"{suit['shadow']}；这张牌的阴影面也可能表现为{rank_shadow}。",
        "mood_angle": f"{suit['mood']} {rank_hint}",
        "weather_oracle_hint": f"{suit['weather']} {rank_hint}",
        "fortune_style": suit["style"],
    }


def build_tarot_card_meta() -> Dict[str, dict]:
    cards = [_major_card(card) for card in MAJOR_CARDS]
    for suit_id in ("wands", "cups", "swords", "pentacles"):
        for index, rank_data in enumerate(RANKS):
            cards.append(_minor_card(suit_id, index, rank_data))
    return {card["id"]: card for card in cards}


TAROT_CARD_META = build_tarot_card_meta()
